    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked

        rowIndex = jTable1.getSelectedRow();

        if (model.getValueAt(rowIndex, 7).toString().equals("male")) {

            jRadioButton_male1.setSelected(true);
            jRadioButton_female2.setSelected(false);
        } else {
            jRadioButton_female2.setSelected(true);
            jRadioButton_male1.setSelected(false);
        }

        jTextField_id.setText(model.getValueAt(rowIndex, 0).toString());
        jTextField_fname.setText(model.getValueAt(rowIndex, 1).toString());
        jTextField_lname.setText(model.getValueAt(rowIndex, 2).toString());
        jTextField_email.setText(model.getValueAt(rowIndex, 5).toString());
        jTextField_contact.setText(model.getValueAt(rowIndex, 4).toString());
        jTextField_address.setText(model.getValueAt(rowIndex, 6).toString());

        Date bdate;
        try {
            bdate = new SimpleDateFormat("yyyy-MM-dd").parse(model.getValueAt(rowIndex, 3).toString());
            jDateChooser_bdate.setDate(bdate);
        } catch (ParseException ex) {
            System.out.println("error" + ex.getMessage());
        }
        try {
            jTable1.setModel(new DefaultTableModel(null, new Object[]{"Id", "First Name", "Last Name", "Birthdate", "Email", "Contact", "Adress", "Gender"}));
            std.fillStudentJtable(jTable1, jTextField_valtyped.getText());
        } catch (Exception ex) {
            System.out.println("error" + ex.getMessage());
        }
    }//GEN-LAST:event_jTable1MouseClicked

