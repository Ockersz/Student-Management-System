    private void jButton_editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_editActionPerformed
       // validation.emailValiadtion(jTextField_email.getText(), jLabel_error);
        String fname = jTextField_fname.getText();
        String lname = jTextField_lname.getText();
        String contact = jTextField_contact.getText();
        String email = jTextField_email.getText();
        String address = jTextField_address.getText();
        int id = Integer.valueOf(jTextField_id.getText());
        String gender = "";
        if (jRadioButton_female2.isSelected()) {
            gender = "female";
        } else if (jRadioButton_male1.isSelected()) {
            gender = "male";
        }

        if (verifText()) {
            SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
            String bdate = dateFormat.format(jDateChooser_bdate.getDate());
            Student std = new Student();
            try {
                std.insertUpdateDeleteStudent('u', id, fname, lname, bdate, contact, email, address, gender);
                //MainForm.jLabel_stdcount.setText("Student Count = " + Integer.toString(MyFunction.countData("students")));
                jTable1.setModel(new DefaultTableModel(null, new Object[]{"Id", "First Name", "Last Name", "Birthdate", "Email", "Contact", "Adress", "Gender"}));
                std.fillStudentJtable(jTable1, jTextField_valtyped.getText());
                jTextField_id.setText("");
                jTextField_fname.setText("");
                jTextField_lname.setText("");
                jTextField_email.setText("");
                jTextField_contact.setText("");
                jTextField_address.setText("");
                jRadioButton_male1.setSelected(false);
                jRadioButton_female2.setSelected(false);
                jDateChooser_bdate.setDate(null);
            } catch (Exception ex) {
                System.out.println("error" + ex.getMessage());
                JOptionPane.showMessageDialog(null, "error");
            }
        }
    }//GEN-LAST:event_jButton_editActionPerformed
